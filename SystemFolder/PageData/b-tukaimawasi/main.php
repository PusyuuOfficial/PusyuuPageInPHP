          <h3>使いまわすなと言うときに気を付けた方がよい</h3>
          <img src="../../ImageData/mayoi.png" alt="画像" class="img-rs_tyuu" oncontextmenu="return false;" onselectstart="return false;" onmousedown="return false;">
          <p>使いまわしについて思ったのが使いまわすなといった時点で、言葉を使って話すのもダメじゃないか思っていしまったのが事の発端です。なぜ言葉をつかえないか、それは記憶したもので話すため記憶するということは一度記憶するために言葉を使用してしまっているからです。ほかにも問題が起きますそれは、イノベーションを起こせなくなる、なぜかといいますとパクる行為が行えなくなるからですイノベーションではパクってそれにオリジナルティを加えて新たなものを作るためこのイノベーションも起こせなくなります。だから思うのですよ、そのように言ってしまう、社長さんはは成長したくないんだろうなーと。</p>
        </article>
        <article class="article" id="p3">
          <h3>イノベーションとは</h3>
          <p>イノベーションでよくありがちな事は、そん時思いついたものやサービスって意外にあなただけでなく周りの人がすでに見つけてしまったアイデアであることがほとんどであり、そこから一から新たなものを作るのは相当、困難なことです。ですがパクることにより基礎を作りそこにオリジナルティを加えると言う作業を行うことで簡略化することができます。そんな便利なイノベーションですが、日本はパクリはダメというのがありますが、日本では知っている車というのはほかの国で走ってたのをパクリってそれに自分なりの味付けすることによりオリジナルティが生まれるが、現在の法律だとあのwinnyのようなことが起きてしまうのが現状である。こんなはなしで終わりにしてしまうのはなんか癪だと思いますのでイノベーションを使用しているものの例を挙げます、今あなたが使っているウェブサイトも、そのスマホも、特にiPhoneはパクリを続けたからこそ今のブランドがあります。カメラ部品はソニーだし、タッチパネルで操作はノキアからだったりそこからオリジナリティを加えることで今のAppleは価値を持って維持できているのです。ほかにもプログラミング言語で言うライブラリーというのは他人が組み合わせながら作った技術で、さらにそれを使ってウェブサイトやネイティブアプリやプログラミング言語そのものを作るわけです。パクるというのはしっかりオリジナリティを加えることで新しいものを作ることができるのです。</p>
        </article>
