          <h3 id="p2">気を張ることでのメリットデメリット</h3>
          <img src="../../ImageData/mayoi.png" alt="画像" class="img-rs_tyuu" oncontextmenu="return false;" onselectstart="return false;" onmousedown="return false;">
          <p>何事にも事故と言うものは何をするにも付き物です。例えばどこかへ場所を移動する際も、対向車に跳ねられる可能性だってあるし、急に自動車が突っ込んでくることもあります。どうしたらいいか、それは、気を張っておくと言うのが一番効果的だと私は思います（完全な私の中の経験による感想です。）。何故かと言いますと、何事も慣れてくると物事にたいし想像でき、その副作用の安心感により危機察知能力が低下するからです。私は慣れて来たがゆえに確かに交通事故を起こしました。気を張っておくことで他にはどのようなメリットがあるのでしょうか、それは老害になりにくくなります。労害とは自分の価値観を押し付け、他人の価値観を見ずに物事を話す傾向があります（もちろんすべての老人が自分の価値観を押し付けてる訳ではありません。）その結果、人に嫌われてしまいます。ですが気を張っておくことで自分で客観的に見ることができ、自分が何をしてしまっているのかを把握することで自分の価値観を押し付けにくく擦ることが可能です。逆に気を張っておくことによるデメリットはなんでしょう、それは、気を長期的に張っていると疲れてしまうことです。対策法としては気をずっと張るのではなく、メリハリをつけ、気を張っておく場面というのを頭のなかにマッピングしておくことで、オンオフが可能になり不必要な時にオフにすることにより、疲れにくくなります。</p>
