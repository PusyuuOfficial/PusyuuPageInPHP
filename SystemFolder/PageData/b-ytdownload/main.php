          <h2>YouTubeの動画をダウンロードする方法</h2>
          <h3>y2mateを使った方法。</h3>
          <p>まず<a href="https://www.y2mate.com" target="_blank">y2mateへのURL</a>にアクセスします。 次にコピーしたYouTubeの動画URLを　にに貼り付ける。 次にいろいろなサイズでダウンロードする画面が出るのでお好みのサイズを選択 ダウンロードボタンが出るのでdownloadボタンを押す。</p>
