          <h3>何故そんなことをするか</h3>
          <p>アプリをダウンロードさせたりして広告などで金儲けするため。 通知形は通知を許可すると通知画面に(あなたのスマホはウィルスに感染しました)見たいな通知を出し、 ブラウザ経由で表示させて、不安を煽り偽ウィルス画面に誘導させると言う目的です。 ですが怖がる必要はありません。眺めるだけ、すぐに閉じる。などさえ守れば何もおきません。</p>
          <h2>もっと安全にYouTubeをダウンロードできないのか</h2>
          <h3>offlibertyを使った方法</h3>
          <p>まず<a href="http://offliberty.io" target="_blank">offlibertyへのURL</a>にアクセスします。 次にコピーしたYouTubeの動画URLを入力ボックスに貼り付ける。 　次にOFFを押し待つそうすると黄色いボタンが二つ出てくるので、 ※上が動画ダウンロードボタン、下が音声ダウンロードボタン（音声ダウンロードは少し待つすると一つ黄色いボタンが出るのでそれを押す）</p>
          <h3>クレイビングエクスプローラーを使った方法</h3>
          <p>クレイビングエクスプローラーを使うと警告は出てきません。 まずインストールしてない場合はこちらのURLから<a href="https://www.crav-ing.com/" target="_blank">クレイビングエクスプローラーへのURL</a>ダウンロードしてください。 そしたらダウンロードしたexeファイルを開きますするとまず管理者権限を聞かれるので、 はいを押しますそしたらインストーラーが開きますので利用規約をよく読み同意するを押します。 次へを押します。そしたらお勧めのアプリを聞かれます。私の場合はすべてはずしました。 ※はずさないと余計なアプリがはいてしまいます。次へを押します。 そしたらインストール詳細画面が出るのでインストールを押します。 まずコピーした圧縮したい動画のURLをURL入力欄に貼り付ける。 そしたら右上のパズルピースの横のダウンロードアイコンをクリック そして標準画質をクリックする。</p>
