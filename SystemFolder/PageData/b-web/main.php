          <h2>ウェブサイトの作り方</h2>
          <h3>ウェブサイトを作る方法は2つぐらいあります。</h3>
          <ol class="marukado_waku">
            <li>googleサイト、wix.comなどのウェブサイトを簡単に作ることができるサービス</li>
            <li>htmlを使って、サーバーにアップロードする方法</li>
          </ol>
          <h3>簡単にウェブ作れる、サービスのメリット</h3>
          <ol class="marukado_waku">
            <li>サーバーを立てなくてよい</li>
            <li>専門的知識がいらない</li>
            <li>簡単に作れる</li>
          </ol>
          <h3>簡単にウェブ作れる、サービスのデメリット</h3>
          <ol class="marukado_waku">
            <li>有料なものが含まれる場合がある。</li>
            <li>自分に合うテンプレートがない場合がある。</li>
            <li>細かな動作が効かない</li>
          </ol>
          <h3>htmlを使うメリット</h3>
          <ol class="marukado_waku">
            <li>細かな動作ができる。</li>
            <li>専門知識を勉強できる。</li>
            <li>自分にしかないデザインを作ることができる。</li>
          </ol>
          <h3>htmlを使うデメリット</h3>
          <ol class="marukado_waku">
            <li>レイアウトが崩れてしまう</li>
            <li>自分でサーバーを探さなければいけない</li>
          </ol>
