          <h3>使いやすかったAndroid-X86派生一覧</h3>
          <h4>Prime OS</h4>
          <p>プライムＯＳは私が試したAndroid-x86のＯＳの中では特に使いやすかったと思います。理由はシステムのカスタマイズが可能なところです。 このＯＳはもともとＲＯＯＴ化されており、もとから入っているアプリを消去したり、システムアプリ化することで初期化しても戻るようにしたりする事ができます。 更に、標準のAndroid-x86では動かない（x64 EFIのみではありますが）アプリも多少は動くように改良がなされてました。 そんな感じで、まるで馬乗りの言うことをある程度は聞いてくれるＯＳでした。ですが流石にWindowsやLinuxには勝てませんでした。</p> <a href="https://www.primeos.in/" target="_blank">Prime OSのダウンロードリンクはこちら</a>
          <h4>Android-x86</h4>
          <p>このOSはほぼAndroidそのままで、アンドロイドに詳しい方であれば、使いにくいのは変わりないかもしれませんが使いこなすことは可能です。 このOSで試して、バグなく動くことを試すことができたアプリはマインクラフトでした。特に落ちることなくPC版同様の操作ができました。</p> <a href="https://www.android-x86.org/" target="_blank">Android-x86のダウンロードリンクはこちら</a> </article>
        <article class="article" id="p3">
          <h3>まとめ</h3>
